		<b>В 2005 году получено свидетельство о государственной регистрации на программный продукт «Автоматизированная информационная система управления энергосбытовой деятельностью «ЭнергоОфис».</b> Продукт ГК ИНФОПРО: ЭнергоОфис, становится стандартом для ОАО «Средневолжской Межрегиональной Управляющей Компании».
	</p>
	<p style="text-align: left;">
		 ОАО «Волжская Территориальная Генерирующая Компания» автоматизирует сбыт тепловой энергии на базе программного продукта ИНФОПРО «Автоматизированная информационная система управления энергосбытовой деятельностью «ЭнергоОфис». <b>В 2006 году расширена география продаж программного продукта «ЭнергоОфис» – подписаны договора с ООО «Энерго-Газ» (г. Ноябрьск) и ОАО «Ставропольэнергосбыт».</b>
