	</p>
	<p style="text-align: left;">
		 На смену АС ASTEP приходит новый продукт – программный комплекс «ИНФОПРО: Планирование режимов», созданный на новейшей технологической платформе и обладающий современной архитектурой. Проект реализуется для ОАО «Иркутскэнерго».<b> В 2008 году ГК ИНФОПРО расширяет доли рынка за счет малых энергосбытовых компаний</b> — ООО «Сбыт-энерго», ЗАО «Кинельэнерго», ООО «НК-Энергосбыт».
	</p>
	<p style="text-align: left;">
		<b>В 2009 году положено начало сотрудничества с группой ИНТЕР РАО — Сочинская ТЭС, </b>Верхнетагильская и Каширская ГРЭС выбрали программный комплекс «ИНФОПРО: Планирование режимов» для автоматизации расчета ТЭП.
	</p>
	<p style="text-align: left;">
		 Разработанная подсистема оптимизации загрузки основного оборудования ТЭС в программном продукте «Планирование режимов» успешно проходит испытания на Набережночелнинской ТЭЦ. ГК ИНФОПРО расширяет направления деятельности за счет услуг по проведению энергетического обследования предприятий.
	</p>
	<p style="text-align: left;">
		<b>В 2011 году для ОАО «Южной Межрегиональной Энергетической Компании» ГК ИНФОРПО разрабатывает систему по работе на оптовом рынке электроэнергии и мощност</b>и — зарождение продуктового направления «ИНФОПРО: Энерготрейдинг». Так же ГК ИНФОПРО расширяет географию: продукт «ИНФОПРО: Планирование режимов» внедряется на ОАО «ТГК-16» и ООО «Башкирская Генерирующая Компания». Оба заказчика внедряют подсистему оптимизации режимов работы.
	</p>
	<p style="text-align: left;">
