	</p>
	<p style="text-align: left;">
		 Стартовал первый проект по внедрению «ИНФОПРО: Энерготрейднг» в сбытовой компании ООО «Торговый дом «Энергосервис» (ООО «ЛУКОЙЛ-ЭНЕРГОСЕРВИС»). Продолжено развитие компетенций - ГК ИНФОПРО автоматизирует деятельность на оптовом рынке электроэнергии и мощности в ООО «Транснефтьэнерго». ГК ИНФОПРО работает от Калининграда до Сахалина — заключен договор с ОАО “Сахалинэнерго” на автоматизацию Южно-Сахалинской ТЭЦ-1.
