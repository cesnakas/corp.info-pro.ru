		<b>В 2014 году с нами сотрудничают предприятия электросетевого комплекса РФ - ОАО «МРСК Центра и Приволжья» - филиал Ивэнерго</b>, ОАО «МРСК Волги» - филиал Оренбургэнерго, ОАО «Иркутская электросетевая компания». Внедрен продукт «ИНФОПРО: Энерготрейдинг» для ОАО «Мосэнергосбыт» (Группа ИНТЕР РАО) и ООО «Симбирская энергосбытовая номинация».
	</p>
	<p style="text-align: left;">
		 Продукты ИНФОПРО, теперь и в атомной энергетике: Ново-Воронежская АЭС (филиал ОАО «Росэнергоатом») выбрала «ИНФОПРО: Планирование режимов» для автоматизации расчета технико-экономических показателей. Завершено создание системы энергоменеджмента в ПАО «РАО ЭС Востока» - появление нового направления «ИНФОПРО: Энергоменеджмент».
	</p>
