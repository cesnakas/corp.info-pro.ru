	<p style="text-align: left;">
		 К моменту образования Группы компаний ИНФОПРО, за плечами коллектива уже были масштабные проекты по автоматизации бизнес-планирования, анализа производственно-финансовой деятельности, формированию и консолидации отчетности ОАО «Самараэнерго».
	</p>
	<p style="text-align: left;">
		<b>В 2003 году ГК ИНФОПРО становится партнером Oracle Corp.</b> Начат проект по созданию информационно-аналитической системы для ОАО «Мосэнерго» на базе программного комплекса «САБиБ» - это система бизнес-моделирования и анализа. «САБиБ», помогает администрации Самарской области формировать отчетность о топливно-энергетическом балансе региона.
